<script setup lang="ts">
import { ref, computed, onMounted } from 'vue'
interface Props {
  type: string
  message?: string
  showtime?: number
}

const csgMessage = ref()

var timer: any = null
const props = withDefaults(defineProps<Props>(), {
  type: 'default',
  message: '默认信息',
  showtime: 1500
})

const messageStyle = computed(() => {
  let bgStyle: string
  switch (props.type) {
    case 'success':
      bgStyle = 'success-bg'
      break

    case 'warn':
      bgStyle = 'warn-bg'
      break

    case 'danger':
      bgStyle = 'danger-bg'
      break

    default:
      bgStyle = 'default-bg'
      break
  }

  return bgStyle
})

const handleMouseover = () => {
  clearTimeout(timer)
}

const handleMouseleave = () => {
  timer = setTimeout(() => {
    let node = csgMessage.value.parentNode
    node.parentNode.removeChild(node)
  }, props.showtime)
}

onMounted(() => {
  timer = setTimeout(() => {
    let node = csgMessage.value.parentNode
    node.parentNode.removeChild(node)
  }, props.showtime)
})
</script>
